type TranslationKey = keyof typeof en;
type TranslationMap = Record<TranslationKey, string>;
type Locale = 'en' | 'zh-CN';
type Replacements = Record<string, string | number>;

const translations: Record<Locale, TranslationMap> = { en, 'zh-CN': zhCN };
const localeStorageKey = 'solo.locale';
const defaultLocale: Locale = 'en';

export const languageOptions: Array<{ value: Locale; label: string }> = [
  { value: 'en', label: 'English' },
  { value: 'zh-CN', label: '中文' },
];

function isLocale(l: string): l is Locale {
  return l in translations;
}

function readStoredLocale(): Locale {
  if (typeof window === 'undefined') return defaultLocale;
  try {
    const stored = window.localStorage.getItem(localeStorageKey);
    return stored && isLocale(stored) ? stored : defaultLocale;
  } catch {
    return defaultLocale;
  }
}

let locale: Locale = defaultLocale;

export function getLocale(): Locale {
  return locale;
}

export function initLocaleFromStorage(): boolean {
  const stored = readStoredLocale();
  if (stored === locale) return false;
  return setLocale(stored);
}

export function setLocale(l: string): boolean {
  if (!isLocale(l)) return false;
  locale = l;
  if (typeof window !== 'undefined') {
    try {
      window.localStorage.setItem(localeStorageKey, l);
    } catch {
      // Ignore storage failures; the in-memory locale still changes.
    }
    document.documentElement.lang = l;
    window.dispatchEvent(new Event('solo:locale-change'));
  }
  return true;
}

export function t(key: TranslationKey, replacements?: Replacements): string {
  let text: string = translations[locale]?.[key] ?? en[key] ?? key;
  if (replacements) {
    for (const [k, v] of Object.entries(replacements)) {
      text = text.replace(`{${k}}`, String(v));
    }
  }
  return text;
}

export type { Locale, TranslationKey };
