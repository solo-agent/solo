members AS (
  SELECT c.template_id,
         c.coordinator_ref,
         template.name AS template_name,
         member.value AS member,
         member.ordinality::int AS position
    FROM coordinators c
    JOIN agent_templates template ON template.id = c.template_id
    CROSS JOIN LATERAL jsonb_array_elements(template.members) WITH ORDINALITY AS member(value, ordinality)
),
coordinator_members AS (
  SELECT template_id, member
    FROM members
   WHERE member->>'ref' = coordinator_ref
),
assignment_edges AS (
  SELECT m.template_id,
         0 AS kind,
         m.position,
         jsonb_build_object(
           'from_ref', m.coordinator_ref,
           'to_ref', m.member->>'ref',
           'type', 'assigns_to',
           'weight', 1,
           'instruction', format(
             '委托条件：当「%s」的频道目标需要%s的专业判断或产出时，由%s委托给%s。回报要求：提交可直接整合的结论和产物，并标明依据、风险与未决事项。',
             m.template_name,
             m.member->>'role',
             coordinator.member->>'name',
             m.member->>'name'
           )
         ) AS relationship
    FROM members m
    JOIN coordinator_members coordinator USING (template_id)
   WHERE m.member->>'ref' <> m.coordinator_ref
),
collaboration_edges AS (
  SELECT template_id,
         1 AS kind,
         position,
         jsonb_build_object(
           'from_ref', from_ref,
           'to_ref', to_ref,
           'type', 'collaborates_with',
           'weight', 1,
           'instruction', instruction
         ) AS relationship
    FROM collaborations
),
all_edges AS (
  SELECT * FROM assignment_edges
  UNION ALL
  SELECT * FROM collaboration_edges
),
relationship_sets AS (
  SELECT template_id, jsonb_agg(relationship ORDER BY kind, position) AS relationships
    FROM all_edges
   GROUP BY template_id
)
UPDATE agent_templates template
   SET relationships = relationship_sets.relationships
  FROM relationship_sets
 WHERE template.id = relationship_sets.template_id;
